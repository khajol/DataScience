
# Identifiers and Variables in Python

"""
In programming, an identifier is a name used to identify a variable, function, class, or any other user-defined item. 
It is a fundamental concept that enables us to assign meaning to different objects in our code. In Python, identifiers can 
be used to represent variables, functions, classes, and other elements of the code structure.

An identifier must adhere to the following rules:
1. It must begin with a letter (a-z, A-Z) or an underscore (_).
2. The subsequent characters can be letters, digits (0-9), or underscores.
3. Identifiers are case-sensitive in Python, which means num and Num are considered different.

Variables, on the other hand, are used to store data. A variable is essentially a container for storing data values. 
Variables allow us to assign values to memory locations, and we can retrieve or modify these values as needed.

Syntax for Assigning Variables:
variable_name = value

Python is a dynamically typed language, meaning that you do not need to declare the type of the variable when assigning a value. 
The interpreter automatically determines the type based on the value assigned.
"""

# Example:
num = 10   # An integer
age = 24   # An integer
mark = 40.5 # A floating-point number
name = "Asna" # A string

