# NumPy Introduction

"""

What is NumPy?  
NumPy (Numerical Python) is a powerful Python library for numerical computing. It provides support for large, multi-dimensional arrays and matrices, along with a collection of mathematical functions to operate on these arrays efficiently.

Why Use NumPy?  
- Faster computations compared to Python lists  
- Provides powerful array manipulation tools  
- Supports mathematical, statistical, and linear algebra functions  
- Forms the foundation for libraries like Pandas, SciPy, and Scikit-learn  

Key Features  
- N-dimensional array objects  
- Broadcasting capabilities  
- Linear algebra and random number generation  
- Optimized for performance  

"""


import numpy as np

# Creating a NumPy array
a = np.array([4, 3, 4, 5])

print(a)  # Output: [4 3 4 5]
