#Compound assignment

# +=  -=  *=  /=  %=  **=  //=


# Initial values
num1 = 10
num2 = 5
num3 = 3
num4 = 2

# += (Addition Assignment)
num1 += 5  # Equivalent to num1 = num1 + 5
print("After +=, num1 =", num1)

# -= (Subtraction Assignment)
num2 -= 2  # Equivalent to num2 = num2 - 2
print("After -=, num2 =", num2)

# *= (Multiplication Assignment)
num3 *= 4  # Equivalent to num3 = num3 * 4
print("After *=, num3 =", num3)

# /= (Division Assignment)
num4 /= 2  # Equivalent to num4 = num4 / 2
print("After /=, num4 =", num4)

# %= (Modulus Assignment)
num1 %= 4  # Equivalent to num1 = num1 % 4
print("After %=, num1 =", num1)

# **= (Exponentiation Assignment)
num2 **= 3  # Equivalent to num2 = num2 ** 3
print("After **=, num2 =", num2)

# //= (Floor Division Assignment)
num3 //= 2  # Equivalent to num3 = num3 // 2
print("After //=, num3 =", num3)
