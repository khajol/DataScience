# Relational operators - used for comparison
# >  <  >=  <=  ==  !=   - output will be True or False

num1 = 10
num2 = 20

# Comparing num1 and num2 using relational operators
print("num1 > num2:", num1 > num2)   # Greater than operator, checks if num1 is greater than num2
print("num1 < num2:", num1 < num2)   # Less than operator, checks if num1 is less than num2
print("num1 >= num2:", num1 >= num2) # Greater than or equal to operator, checks if num1 is greater than or equal to num2
print("num1 <= num2:", num1 <= num2) # Less than or equal to operator, checks if num1 is less than or equal to num2
print("num1 == num2:", num1 == num2) # Equal to operator, checks if num1 is equal to num2
print("num1 != num2:", num1 != num2) # Not equal to operator, checks if num1 is not equal to num2

# Comparison between boolean values
print("True < False:", True < False)  # True is treated as 1, False as 0, so True < False is 1 < 0, which gives False
print("True > False:", True > False)  # True is treated as 1, False as 0, so True > False is 1 > 0, which gives True
